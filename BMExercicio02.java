class BMExercicio02 {
    public static void main(String[] args) {
    	int i = 1;
    	System.out.println("Inteiro i de 32 bits com valor " + i);
    	
	long j = 2; 
	System.out.println("Inteiro j de 64 bits com valor " + j);
	
	double p = 20.0; 
	System.out.println("Ponto flutuante p de 32 bits com valor " + p);
	
	float q = 30.0f; 
	System.out.println("Ponto flutuante q de 64 bits com valor " + q);
	
	boolean b = true;
	System.out.println("Boolean b com valor " + b);
	
	char c = 'k'; 
	System.out.println("Caracter c com valor " + c);
    }
}
