/* Crie um programa e declare nele 3 variáveis numéricas int, inicializando-as com algum valor
* e obtenha a soma dessas variáveis e a média delas, imprimindo esses resultados na tela.
*/

public class CBBAtividadePratica2 {
    public static void main(String[] args) {
        int numero1 = 5, numero2 = 8, numero3 = 2;
        System.out.println("Soma das variáveis: " + (numero1 + numero2 + numero3));
        System.out.println("Média das variáveis: " + (numero1 + numero2 + numero3) / 3);
    }
}